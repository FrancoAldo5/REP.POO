    public void mostrarDatos() {
        String[] titulos = {"Codigo", "Nombre", "Apellido", "Asignatura", "Promedio", "Estado"};
        String[] registros = new String[6];
        DefaultTableModel mod = new DefaultTableModel(null, titulos);
        DBCollection colec = db.getCollection("estudiantes");//La que crea la tabla de la base de datos
        DBCursor cursor = colec.find();
        while (cursor.hasNext()) {
            BasicDBObject doc = (BasicDBObject) cursor.next();
            registros[0] = "" + doc.get("_Codigo");
            registros[1] = "" + doc.get("Nombre");
            registros[2] = "" + doc.get("Apellido");
            registros[3] = "" + doc.get("Asignatura");
            registros[4] = "" + doc.get("Promedio");
            registros[5] = "" + doc.get("Estado");
            mod.addRow(registros);
        }
        btntabla.setModel(mod);// Establece el modelo de la tabla
        btntabla.setDefaultEditor(Object.class, null);// Deshabilita la edici칩n directa en la tabla
    }
    
    public void guardarDatos() {
        DBCollection colec = db.getCollection("estudiantes");
        BasicDBObject documento = new BasicDBObject();
        documento.put("Nombre", btnnombre.getText());
        documento.put("Apellido", btnapellido.getText()); 
        documento.put("Asignatura", btnasignatura.getSelectedItem());
        documento.put("Promedio", Integer.parseInt(btnpromedio.getText()));
        documento.put("Estado", btnestado.getSelectedItem());
        colec.insert(documento);
    }
    
