    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        
try {
        Document document = new Document(PageSize.A4.rotate());
        String destino = "registro estudiante.pdf";  // Nombre del archivo PDF
        PdfWriter.getInstance(document, new FileOutputStream(destino));
        document.open();

        // Agregar t칤tulo al PDF
        Font fontTitle = FontFactory.getFont(FontFactory.HELVETICA_BOLD, 18);
        Paragraph title = new Paragraph("Tabla de Registros de Estudiantes", fontTitle);
        title.setAlignment(Paragraph.ALIGN_CENTER);
        document.add(title);

        // Agregar tabla al PDF
        com.itextpdf.text.pdf.PdfPTable pdfTable = new com.itextpdf.text.pdf.PdfPTable(btntabla.getColumnCount());
        pdfTable.setWidthPercentage(100);

        // Agregar encabezados de columna
        for (int i = 0; i < btntabla.getColumnCount(); i++) {
            pdfTable.addCell(new Paragraph(btntabla.getColumnName(i)));
        }

        // Agregar filas de la tabla
        for (int rows = 0; rows < btntabla.getRowCount(); rows++) {
            for (int cols = 0; cols < btntabla.getColumnCount(); cols++) {
                pdfTable.addCell(new Paragraph(btntabla.getModel().getValueAt(rows, cols).toString()));
            }
        }
        
        document.add(pdfTable);
        document.close();
        
        System.out.println("PDF generado exitosamente");

    } catch (DocumentException | FileNotFoundException ex) {
        ex.printStackTrace();
    }// TODO add your handling code here:
    }//GEN-LAST:event_jButton4ActionPerformed

