    public void mostrarDatos() {
        String[] titulos = {"Codigo", "Hora", "Encargado", "Laboratorio", "Permiso"};
        String[] registros = new String[5];
        DefaultTableModel mod = new DefaultTableModel(null, titulos);
        DBCollection colec = db.getCollection("horario");//La que crea la tabla de la base de datos
        DBCursor cursor = colec.find();
        while (cursor.hasNext()) {
            BasicDBObject doc = (BasicDBObject) cursor.next();
            registros[0] = "" + doc.get("_Codigo");
            registros[1] = "" + doc.get("Hora");
            registros[2] = "" + doc.get("Encargado");
            registros[3] = "" + doc.get("Laboratorio");
            registros[4] = "" + doc.get("Permiso");
            mod.addRow(registros);
        }
        tablaa.setModel(mod);// Establece el modelo de la tabla
        tablaa.setDefaultEditor(Object.class, null);// Deshabilita la edici칩n directa en la tabla
    }
    
    public void guardarDatos() {
        DBCollection colec = db.getCollection("horario");
        BasicDBObject documento = new BasicDBObject();
        documento.put("Hora", btnhora.getSelectedItem());
        documento.put("Encargado", btnencargado.getText()); 
        documento.put("Laboratorio", btnlaboratorio.getSelectedItem());
        documento.put("Permiso", btnpermiso.getSelectedItem());
        colec.insert(documento);
    }
    
